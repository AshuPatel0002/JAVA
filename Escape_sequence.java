public class Escape_sequence {
    public static void main(String[] args) {
        // insert a tab in the text at this point
        System.out.println("welcome to\tindia");
        //insert a backspace in the text at this point
        System.out.println("welcome to\bindia ");
        //insert a newline in the text at this point
        System.out.println("welcome to\nindai");
        //insert a single quote character in the text at this point
        System.out.println("welcome to\' 'india'");
        //insert  a double quote character in the text at this point
        System.out.println("welcome to\" india");
        //inssert a backslash character in the text at this point
        System.out.println("welcome to\\ india");
    }
}

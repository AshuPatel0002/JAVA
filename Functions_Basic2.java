public class Functions_Basic2 {
    public static void main(String[] args) {
        Firstpattern();Secondpattern();thirdPattern();
    }

    public static void Firstpattern(){
        System.out.println("*");
        System.out.println("* *");
        System.out.println("* * *");
        System.out.println("* * * *");
        System.out.println("* * * * *");
        System.out.println("============");
    }

    public static void Secondpattern(){
        System.out.println("* * * * *");
        System.out.println("* * * *");
        System.out.println("* * *");
        System.out.println("* *");
        System.out.println("*");
        System.out.println("===========");
    }


    public static void thirdPattern(){
        System.out.println("           *");
        System.out.println("         * *");
        System.out.println("       * * *");
        System.out.println("     * * * *");
        System.out.println("   * * * * *");
    }
}
